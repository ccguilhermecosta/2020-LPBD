DROP TABLE DEPARTAMENTO;
CREATE TABLE DEPARTAMENTO(
dep_id INTEGER PRIMARY KEY,
dep_nome VARCHAR2(100) NOT NULL,
dep_ramal NUMBER(4) NOT NULL,
empr_id INTEGER
    CONSTRAINT empr_id_FK REFERENCES EMPRESA(empr_id)
);

DROP TABLE EMPRESA;
CREATE TABLE EMPRESA(
empr_id INTEGER PRIMARY KEY,
empr_nome VARCHAR2(100),
empr_ramo VARCHAR2(50)
);

DROP TABLE FUNCIONARIO;
CREATE TABLE FUNCIONARIO(
fun_id INTEGER NOT NULL PRIMARY KEY,
fun_nome VARCHAR2(100),
fun_nascimento DATE,
fun_sexo CHAR(1),
dep_id INTEGER NOT NULL
    CONSTRAINT dep_id_FK REFERENCES DEPARTAMENTO(DEP_ID)
);


